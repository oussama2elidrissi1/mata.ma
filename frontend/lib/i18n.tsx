const LanguageContext = createContext<{
  language: Language
  setLanguage: (lang: Language) => void
  t: (key: string) => string
}>({
  language: 'fr',
  setLanguage: () => {},
  t: () => '',
})

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguageState] = useState<Language>('fr')

  useEffect(() => {
    // Récupérer la langue depuis localStorage ou utiliser 'fr' par défaut
    const savedLang = localStorage.getItem('language') as Language
    if (savedLang && ['fr', 'en', 'ar'].includes(savedLang)) {
      setLanguageState(savedLang)
    }
  }, [])

  const setLanguage = (lang: Language) => {
    setLanguageState(lang)
    localStorage.setItem('language', lang)
    // Changer la direction du document pour l'arabe
    if (typeof document !== 'undefined') {
      document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr'
      document.documentElement.lang = lang
    }
  }

  const t = (key: string): string => {
    const keys = key.split('.')
    let value: any = translations[language]
    
    for (const k of keys) {
      value = value?.[k]
      if (value === undefined) {
        // Fallback vers le français si la clé n'existe pas
        value = translations.fr
        for (const k2 of keys) {
          value = value?.[k2]
        }
        break
      }
    }
    
    // Si la valeur est un objet, retourner la clé originale
    if (typeof value === 'object' && value !== null) {
      return key
    }
    
    return typeof value === 'string' ? value : key
  }

  useEffect(() => {
    // Appliquer la direction et la langue au chargement
    if (typeof document !== 'undefined') {
      document.documentElement.dir = language === 'ar' ? 'rtl' : 'ltr'
      document.documentElement.lang = language
    }
  }, [language])

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  )
}

export function useTranslation() {
  return useContext(LanguageContext)
}
